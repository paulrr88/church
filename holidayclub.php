				<h1>Summer Holiday Club 2016</h1><br />

				<div id="hcparagraphs">
					<p>Holiday Club at Ferring Baptist Church will run from Tuesday 9th August until Friday 12th August.  The sessions will be from 10am – 12 noon on all four mornings and there will be a Grand Finale and buffet on the Friday beginning at 6pm to which families are invited.  The whole club is free of charge, apart from tickets for food on Friday evening which are sold during the week.  All sessions are led by an experienced team who include Father Gary and myself, and every group leader will have obtained the necessary disclosure to work with children.</p>

					<p>“Rocky’s Plaice” is based on comedy adventures in a fish-and-chip shop, intertwined with the telling of Bible stories at “Chip Shop Church”.  Each story is presented on film, and daily activities include games, songs, quizzes and crafts. Games are outdoors whenever weather allows, and safe footwear (eg trainers rather than flip-flops) is essential every day.  Games on Friday morning will involve a certain amount of water, but if you do not want your child to get wet you can indicate this on the form below.  We also include Holiday Club photographs in the publicity on our church’s website, and it is possible that your child’s face might appear in one of these.  Again, if you do not wish this to happen you can refuse permission on the form below.</p>

					<p>Children aged 5-11 yrs are welcome and, while not essential, it helps a lot if you register your child(ren) before the club begins, in order for us to assess the numbers likely to attend.  You can do this by filling in the form below and posting it to us at the church, or putting it through the church letter-box when passing by.  All children must have the consent of a parent (or adult with legal parental responsibility).  It is essential that all of the details required on the form are filled in, using a separate form for each child (ring me if more forms are needed, or after the end of term these can be downloaded from the church website).</p>
				</div>

