				<h1>Summer Holiday Club 2014</h1><br />
				
				<div id="hcparagraphs">

					<p>This year&#8217;s club will be held from <strong>Tuesday 12th to Friday 15th August</strong> as stated above, and our <strong>sessions will be from 10am &#8211;12 noon on all four mornings</strong>. <strong>Children aged 5&#8211;11</strong> are welcome. There will also be a Grand Finale and buffet on the Friday beginning at 6pm to which families are invited. The whole club is free of charge, apart from <strong>tickets for food on Friday evening</strong> which are sold during the week. All sessions are led by an <strong>experienced team who include Father Gary and Rev Stuart Robinson</strong> (our pastor), and every adult will have obtained the necessary disclosure to work with children.</p>
					
					<a name="form"></a>

					<p>Based on the theme of recycling, <strong>“Waste Watchers” is about change and renewal</strong>. Each part of the story is presented on film, and daily activities include games, songs, quizzes and crafts. <strong>Games are outdoors</strong> whenever weather allows, and <strong>safe footwear</strong> (eg trainers rather than flip-flops) is essential every day. <strong>Games on Friday morning will involve a certain amount of water</strong>, but if you do not want your child to get wet you can indicate this on the form below. <strong>We also include Holiday Club photographs in the publicity on this website</strong>, and it is possible that your child’s face might appear in one of these. Again, if you do not wish this to happen you can refuse permission on the form below.</p>

					<p>While not essential, <strong>it helps a lot if you register your child(ren) before the club begins</strong>, in order for us to assess the numbers likely to attend. You can do this by filling in the form below and posting it to us at the church, or putting it through the church letter-box when passing by. </p>

					<p>All children must have the consent of a parent (or adult with legal parental responsibility).</p>

					<p>If you have any queries, please do not hesitate to <a href="inquiries.php">contact us</a>!</p>
				</div>
				
