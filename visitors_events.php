				<p>Prayer is of course at the heart of everything we do but on alternate Wednesdays at 3:00 P.M. we gather for an hour of thanksgiving and prayer for the wider world, for our local community, and for the work of our church and those of our congregation who have particular needs. These times are vital to the life of the whole fellowship and a great encouragement to those who take part.</p>
							
